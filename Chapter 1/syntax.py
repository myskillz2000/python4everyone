""" Basic syntax of building blocks of python """

x = 2 # Assignment statement
x = x + 2 # Assignment with expression
print(x) # print statement

""" Conditional example """

x = 5 # Assignment
if x < 10: # if is a conditional statement, make sure you have a colon here.
    print('Smaller')
if x > 20:
    print('Bigger')

print('Finis')

""" Repeated Steps """

n = 5
while n > 0:
    print(n)
    n = n-1
print('Blastoff')